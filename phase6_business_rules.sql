-- Kerakli kengaytmani faollashtiramiz (agar yo'q bo'lsa)
CREATE EXTENSION IF NOT EXISTS pg_cron;

-- QOIDA 3: Har kuni soat tungi 00:00 da aktiv obunachilarga yangi kunlik vazifa (Today Tasks) yaratish.
-- Avval eski cron'ni o'chiramiz
SELECT cron.unschedule('generate_daily_tasks');
-- Yangi cron yaratamiz
SELECT cron.schedule('generate_daily_tasks', '0 0 * * *', $$
    INSERT INTO public.today_tasks (user_id, task_type, task_title, is_completed)
    SELECT user_id, 'video', 'Kunlik majburiy darsni ko''ring', false
    FROM public.user_subscriptions
    WHERE is_active = true AND end_date >= now();
$$);


-- QOIDA 4: Har kuni ertalab 08:00 da obunasi 3 kundan keyin tugaydigan foydalanuvchilarga bildirishnoma yuborish.
SELECT cron.unschedule('warn_expiring_subs');
SELECT cron.schedule('warn_expiring_subs', '0 8 * * *', $$
    INSERT INTO public.notifications (user_id, title, body, type)
    SELECT user_id, 'Obuna yakunlanmoqda', 'Sizning obunangiz 3 kundan so''ng tugaydi. Kurslarni davom ettirish uchun to''lov qiling.', 'system'
    FROM public.user_subscriptions
    WHERE is_active = true AND end_date::date = (now() + interval '3 days')::date;
$$);


